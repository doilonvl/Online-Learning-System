package model;

public class SubjectCategory {
    private int id;
    private String name;

    public SubjectCategory() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "SubjectCategory{" + "id=" + id + ", name=" + name + '}';
    }
    
}